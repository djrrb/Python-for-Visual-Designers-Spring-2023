# testing for truth or false

print('world' == 'world')  # equivalence
print('h' in 'world') # contains
print('h' not in 'world') # does not contain

print(5 > 2) # greater than
print(5 <= 2) # less than or equal to
print(5 != 10) # not equal to
print(5 == 10 and 10/2 == 5) # all tests must be true
print(5 == 10 or 10/2 == 5) # any test can be true
